draw_frame1	.local
	ldy #0
a0	lda (pixphs),y
	bpl	dr0
	inw pixphs
	inw pixcnt
	cpw pixcnt #pix_num-1
	bne a1
	lda #{RTS}
	sta draw_frame1
	sta draw_frame2
	rts
a1	inw sxptrh
	inw sxptrl
	inw syptrh
	inw syptrl
	inw dxptrh
	inw dxptrl
	inw dyptrh
	inw dyptrl
	jmp a0
dr0
	ldy ry
.rept pic_pck #
	lda (pixphs),y
	sta genclr
	cmp #pic_phs-1
	bne	l:1
	lda #0				// if phase = 0 then not clear
	sta clear_frame1.l:1+1
	sta clear_frame1.l:1+3
	lda (sxptrh),y
	tax
	lda (syptrh),y
	tay
	lda vram_l1,y
	sta vramp1
	sta vramp2
	lda vram_h1,y
	sta vramp1+1
	lda vram_h2,y
	sta vramp2+1	
	ldy pixbyte,x
	lda (vramp1),y
	and masktab,x
	sta (vramp1),y	
	lda (vramp2),y
	and masktab,x
	sta (vramp2),y		
l:1	ldy ry
	lda (pixphs),y
	sec
	sbc #1
	sta (pixphs),y
	
	clc					// math fixed point
	lda (sxptrl),y
	adc (dxptrl),y
	sta (sxptrl),y
	lda (sxptrh),y
	adc (dxptrh),y
	sta (sxptrh),y
	tax
	clc
	lda (syptrl),y
	adc (dyptrl),y
	sta (syptrl),y
	lda (syptrh),y
	adc (dyptrh),y
	sta (syptrh),y
	tay

	lda pixbyte,x
	clc
	adc vram_l1,y
	sta vramp1
	sta vramp2	
	lda vram_h1,y
	sta vramp1+1
	lda vram_h2,y
	sta vramp2+1
		
	ldy #0
	lda genclr
	bne d:1
	
	sty clear_frame1.l:1+1
	sty clear_frame1.l:1+3
	lda (vramp1),y
	ora pixtab,x
	sta (vramp1),y
	lda (vramp2),y
	ora pixtab,x
	sta (vramp2),y		
	bne j:1

