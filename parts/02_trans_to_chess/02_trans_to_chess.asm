	sta clear_frame1.l:1+1
	ora pixtab,x
	sta (vramp1),y	
	lda vramp1
	sta clear_frame1.l:1+3	
	lda vramp1+1
	sta clear_frame1.l:1+4	

j:1
	
	ldy ry
	iny
	cpy rym
	bne h:1 
e:1	rts
h:1 sty ry	
.endr
	rts
.endl

